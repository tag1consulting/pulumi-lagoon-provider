func TestProjectAutogeneratedRouteConfig_Create_HappyPath(t *testing.T) {
	called := false
	enabled := true
	mock := &mockLagoonClient{
		updateAutoRouteConfigProjectFn: func(_ context.Context, projectName string, patch map[string]any) (*client.AutogeneratedRouteConfig, error) {
			called = true
			if projectName != "my-project" {
				t.Errorf("expected my-project, got %s", projectName)
			}
			if patch["enabled"] != true {
				t.Errorf("expected enabled=true in patch, got %v", patch["enabled"])
			}
			return &client.AutogeneratedRouteConfig{Enabled: &enabled}, nil
		},
	}
	ctx := testCtx(mock)
	r := &ProjectAutogeneratedRouteConfig{}
	resp, err := r.Create(ctx, infer.CreateRequest[ProjectAutogeneratedRouteConfigArgs]{
		Inputs: ProjectAutogeneratedRouteConfigArgs{ProjectName: "my-project", Enabled: &enabled},
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if !called {
		t.Error("expected API to be called")
	}
	if resp.ID != "my-project" {
		t.Errorf("expected ID my-project, got %s", resp.ID)
	}
}

