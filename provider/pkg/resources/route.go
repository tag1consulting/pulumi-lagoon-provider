// Route manages a Lagoon API-defined route on a project, with optional environment attachment.
type Route struct{}

// RouteAnnotationInput is a key/value annotation on a route.
type RouteAnnotationInput struct {
	Key   string `pulumi:"key"`
	Value string `pulumi:"value"`
}

// RoutePathRouteInput is a path-based routing rule on a route.
type RoutePathRouteInput struct {
	Path      string `pulumi:"path"`
	ToService string `pulumi:"toService"`
}

// RouteArgs defines the user-provided inputs for a LagoonRoute resource.
type RouteArgs struct {
	ProjectName                string                 `pulumi:"projectName"`
	Domain                     string                 `pulumi:"domain"`
	Service                    *string                `pulumi:"service,optional"`
	Environment                *string                `pulumi:"environment,optional"`
	TLSAcme                    *bool                  `pulumi:"tlsAcme,optional"`
	Insecure                   *string                `pulumi:"insecure,optional"`
	RouteType                  *string                `pulumi:"routeType,optional"`
	Primary                    *bool                  `pulumi:"primary,optional"`
	MonitoringPath             *string                `pulumi:"monitoringPath,optional"`
	DisableRequestVerification *bool                  `pulumi:"disableRequestVerification,optional"`
	HSTSEnabled                *bool                  `pulumi:"hstsEnabled,optional"`
	HSTSPreload                *bool                  `pulumi:"hstsPreload,optional"`
	HSTSIncludeSubdomains      *bool                  `pulumi:"hstsIncludeSubdomains,optional"`
	HSTSMaxAge                 *int                   `pulumi:"hstsMaxAge,optional"`
	Annotations                *[]RouteAnnotationInput `pulumi:"annotations,optional"`
	AlternativeNames           *[]string              `pulumi:"alternativeNames,optional"`
	PathRoutes                 *[]RoutePathRouteInput  `pulumi:"pathRoutes,optional"`
}

// RouteState is the full persisted state for a LagoonRoute resource.
type RouteState struct {
	RouteArgs
	LagoonID int    `pulumi:"lagoonId"`
	Source   string `pulumi:"source"`
	Created  string `pulumi:"created"`
	Updated  string `pulumi:"updated"`
}

