func TestEnvironmentAutogeneratedRouteConfig_Read_ByImportID(t *testing.T) {
	enabled := true
	mock := &mockLagoonClient{
		getAutoRouteConfigEnvFn: func(_ context.Context, projectName, environmentName string) (*client.AutogeneratedRouteConfig, error) {
			if projectName != "my-project" || environmentName != "main" {
				t.Errorf("unexpected args: %s %s", projectName, environmentName)
			}
			return &client.AutogeneratedRouteConfig{Enabled: &enabled}, nil
		},
	}
	ctx := testCtx(mock)
	r := &EnvironmentAutogeneratedRouteConfig{}
	resp, err := r.Read(ctx, infer.ReadRequest[EnvironmentAutogeneratedRouteConfigArgs, EnvironmentAutogeneratedRouteConfigState]{
		ID:    "my-project:main",
		State: EnvironmentAutogeneratedRouteConfigState{},
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if resp.Inputs.ProjectName != "my-project" {
		t.Errorf("expected projectName my-project, got %s", resp.Inputs.ProjectName)
	}
	if resp.Inputs.EnvironmentName != "main" {
		t.Errorf("expected environmentName main, got %s", resp.Inputs.EnvironmentName)
	}
}

func TestEnvironmentAutogeneratedRouteConfig_Diff_BothIdentityFieldsAreForceNew(t *testing.T) {
	r := &EnvironmentAutogeneratedRouteConfig{}

	// Project name change
	resp, err := r.Diff(testCtx(&mockLagoonClient{}), infer.DiffRequest[EnvironmentAutogeneratedRouteConfigArgs, EnvironmentAutogeneratedRouteConfigState]{
		Inputs: EnvironmentAutogeneratedRouteConfigArgs{ProjectName: "new-proj", EnvironmentName: "main"},
		State:  EnvironmentAutogeneratedRouteConfigState{EnvironmentAutogeneratedRouteConfigArgs: EnvironmentAutogeneratedRouteConfigArgs{ProjectName: "old-proj", EnvironmentName: "main"}},
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if d, ok := resp.DetailedDiff["projectName"]; !ok || d.Kind != p.UpdateReplace {
		t.Errorf("expected projectName UpdateReplace, got %v", resp.DetailedDiff)
	}

	// Environment name change
	resp, err = r.Diff(testCtx(&mockLagoonClient{}), infer.DiffRequest[EnvironmentAutogeneratedRouteConfigArgs, EnvironmentAutogeneratedRouteConfigState]{
		Inputs: EnvironmentAutogeneratedRouteConfigArgs{ProjectName: "p", EnvironmentName: "new-env"},
		State:  EnvironmentAutogeneratedRouteConfigState{EnvironmentAutogeneratedRouteConfigArgs: EnvironmentAutogeneratedRouteConfigArgs{ProjectName: "p", EnvironmentName: "old-env"}},
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if d, ok := resp.DetailedDiff["environmentName"]; !ok || d.Kind != p.UpdateReplace {
		t.Errorf("expected environmentName UpdateReplace, got %v", resp.DetailedDiff)
	}
}
