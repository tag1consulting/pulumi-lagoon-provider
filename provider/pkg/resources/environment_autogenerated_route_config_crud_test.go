func TestEnvironmentAutogeneratedRouteConfig_Create_HappyPath(t *testing.T) {
	called := false
	enabled := true
	mock := &mockLagoonClient{
		updateAutoRouteConfigEnvFn: func(_ context.Context, projectName, environmentName string, patch map[string]any) (*client.AutogeneratedRouteConfig, error) {
			called = true
			if projectName != "my-project" {
				t.Errorf("expected my-project, got %s", projectName)
			}
			if environmentName != "main" {
				t.Errorf("expected main, got %s", environmentName)
			}
			return &client.AutogeneratedRouteConfig{Enabled: &enabled}, nil
		},
	}
	ctx := testCtx(mock)
	r := &EnvironmentAutogeneratedRouteConfig{}
	resp, err := r.Create(ctx, infer.CreateRequest[EnvironmentAutogeneratedRouteConfigArgs]{
		Inputs: EnvironmentAutogeneratedRouteConfigArgs{
			ProjectName:     "my-project",
			EnvironmentName: "main",
			Enabled:         &enabled,
		},
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if !called {
		t.Error("expected API to be called")
	}
	if resp.ID != "my-project:main" {
		t.Errorf("expected ID my-project:main, got %s", resp.ID)
	}
}

